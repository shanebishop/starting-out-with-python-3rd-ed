'''
Assuming there are no accidents or delays, the distance
that a car travels down the interstate can be
calculated with the folowing formala:

  Distance = Speed * Time

A car is traveling at 70 miles per hour. Write a
program that displays the following:
-The distance the car will travel in 6 hours
-The distance the car will travel in 10 hours
-The distance the car will travel in 15 hours
'''

speed = 70
print('Traveled in 6 hours:', speed * 6)
print('Traveled in 10 hours:', speed * 10)
print('Traveled in 15 hours:', speed * 15)
